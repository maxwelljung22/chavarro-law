const config = window.CHAVARRO_LAW_CONFIG || {};
const bookingUrl = config.bookingUrl || "https://calendly.com/your-calendly-link";
const contactEmail = config.contactEmail || "hello@chavarrolaw.com";
const languageToggle = document.getElementById("languageToggle");
const translatableNodes = document.querySelectorAll("[data-i18n]");
const contactForm = document.getElementById("contactForm");
const formStatus = document.getElementById("formStatus");
const bookingLink = document.getElementById("bookingLink");
const bookingFrame = document.querySelector(".calendar-frame iframe");

let currentLanguage = "en";

function t(key) {
  return translations[currentLanguage][key];
}

function applyLanguage(language) {
  currentLanguage = language;

  translatableNodes.forEach((node) => {
    const key = node.dataset.i18n;
    const value = translations[language][key];
    if (value) {
      node.textContent = value;
    }
  });

  document.documentElement.lang = language === "es" ? "es" : "en";
  localStorage.setItem("chavarro-law-language", language);
  languageToggle.setAttribute("aria-pressed", String(language === "es"));
}

function initializeLanguage() {
  const storedLanguage = localStorage.getItem("chavarro-law-language");
  const browserLanguage = navigator.language.toLowerCase().startsWith("es") ? "es" : "en";
  applyLanguage(storedLanguage || browserLanguage);
}

function initializeBooking() {
  bookingLink.href = bookingUrl;
  bookingFrame.src = `${bookingUrl}?hide_event_type_details=1&hide_gdpr_banner=1`;
}

function initializeReveal() {
  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("is-visible");
          observer.unobserve(entry.target);
        }
      });
    },
    { threshold: 0.16 }
  );

  document.querySelectorAll(".reveal").forEach((item) => observer.observe(item));
}

function initializeTilt() {
  const panels = document.querySelectorAll(".monogram-card, .detail-card, .service-card, .credential-card");

  panels.forEach((panel) => {
    panel.classList.add("tilt");

    panel.addEventListener("pointermove", (event) => {
      if (window.matchMedia("(prefers-reduced-motion: reduce)").matches) {
        return;
      }

      const rect = panel.getBoundingClientRect();
      const x = (event.clientX - rect.left) / rect.width;
      const y = (event.clientY - rect.top) / rect.height;
      const rotateY = (x - 0.5) * 8;
      const rotateX = (0.5 - y) * 8;
      panel.style.transform = `perspective(900px) rotateX(${rotateX}deg) rotateY(${rotateY}deg) translateY(-4px)`;
    });

    panel.addEventListener("pointerleave", () => {
      panel.style.transform = "";
    });
  });
}

languageToggle.addEventListener("click", () => {
  applyLanguage(currentLanguage === "en" ? "es" : "en");
});

contactForm.addEventListener("submit", (event) => {
  event.preventDefault();

  const formData = new FormData(contactForm);
  const name = formData.get("name")?.toString().trim() || "";
  const email = formData.get("email")?.toString().trim() || "";
  const phone = formData.get("phone")?.toString().trim() || "";
  const message = formData.get("message")?.toString().trim() || "";

  const subject =
    currentLanguage === "es"
      ? `Consulta legal de ${name || "cliente potencial"}`
      : `Legal consultation from ${name || "prospective client"}`;

  const body = encodeURIComponent(
    currentLanguage === "es"
      ? `Nombre: ${name}\nCorreo: ${email}\nTelefono: ${phone}\n\nMensaje:\n${message}`
      : `Name: ${name}\nEmail: ${email}\nPhone: ${phone}\n\nMessage:\n${message}`
  );

  window.location.href = `mailto:${contactEmail}?subject=${encodeURIComponent(subject)}&body=${body}`;
  formStatus.textContent = t("formOpened");
});

initializeLanguage();
initializeBooking();
initializeReveal();
initializeTilt();

if (bookingUrl.includes("your-calendly-link")) {
  bookingLink.addEventListener("click", (event) => {
    event.preventDefault();
    formStatus.textContent = t("bookingPlaceholder");
  });
}
